num = int(input("Enter a number: "))
if num > 0:
    """
    This is a first condition which prints positive if the number is greater than 0.
    """
    print("The number is positive.")
elif num < 0:
    """
    This is a codition which prints negatuve if the number is less than 0.
    """
    print("The number is negative.")
else:
    """
    This is a condition which prints zero if the number is equal to 0.
    """
    print("The number is zero.")